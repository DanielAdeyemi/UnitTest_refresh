namespace Sparky
{
    public class Calculator
    {
        public int AddNums(int a, int b)
        {
            return a + b;
        }
        public double AddDoubles(double a, double b)
        {
            return a + b;
        }

        public bool isOddNum(int a)
        {
            return a % 2 != 0;
        }
    }
}